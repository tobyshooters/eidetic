static inline uint8_t
px_get(uint8_t* data, int img_width, int x, int y)
{
  return data[(y * img_width + x) * 3];
}

static inline void
px_set(uint8_t* data, int img_width, int x, int y,
       uint8_t r, uint8_t g, uint8_t b)
{
  int idx = (y * img_width + x) * 3;
  data[idx] = r;
  data[idx + 1] = g;
  data[idx + 2] = b;
}

void
write_glyph(uint8_t* data, int img_width, char ch, int x, int y,
             uint8_t r, uint8_t g, uint8_t b)
{
  uint32_t glyph = font_data[(int)ch];
  for (int i = 0; i < FONT_NBITS; i++) {
    int bit = (glyph >> (FONT_NBITS - i - 1)) & 1;
    int cx = i % FONT_WIDTH;
    int cy = i / FONT_WIDTH;
    if (bit) {
      px_set(data, img_width, x + cx, y + cy, r, g, b);
    }
  }
}

uint32_t
read_glyph(uint8_t* data, int img_width, int x, int y)
{
  uint32_t glyph = 0;
  for (int i = 0; i < FONT_NBITS; i++) {
    int cx = i % FONT_WIDTH;
    int cy = i / FONT_WIDTH;
    int val = px_get(data, img_width, x + cx, y + cy) < 128 ? 1 : 0;
    glyph |= ((uint32_t)val) << (FONT_NBITS - 1 - i);
  }
  return glyph;
}

void
write_text(uint8_t* data, int img_width, char* text, int x, int y,
            uint8_t r, uint8_t g, uint8_t b)
{
  int n = strlen(text);
  for (int i = 0; i < n; i++)
    write_glyph(data, img_width, text[i], x + i * (FONT_WIDTH + 1), y, r, g, b);
}

char*
read_text(uint8_t* data, int img_width, int img_height, int x, int y)
{
  static char buffer[256];
  int count = 0;

  int offset = 0;
  while (x + offset + FONT_WIDTH <= img_width && y + FONT_HEIGHT <= img_height) {
    uint32_t glyph = read_glyph(data, img_width, x + offset, y);
    char ch = '\0';
    for (int c = 32; c < 128; c++) {
      if (font_data[c] == glyph) {
        ch = c;
        break;
      }
    }
    if (ch == '\0' || ch == ' ')
      break;
    buffer[count++] = ch;
    offset += FONT_WIDTH + 1;
  }
  if (count == 0)
    return NULL;
  buffer[count] = '\0';
  return buffer;
}
